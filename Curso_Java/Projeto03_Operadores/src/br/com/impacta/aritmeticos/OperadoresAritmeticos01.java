package br.com.impacta.aritmeticos;

public class OperadoresAritmeticos01 {
	public static void main(String[] args) {
		int a = 10, b = 3, c = 5;

		int div1 = a / b;
		double div2 = a / b;
		int resto = a % b;

		System.out.println(div1 + c);
		System.out.println(div2);
		System.out.println(resto);
	}
}
